#include <unistd.h>

char g_pos;

char g_neg;

void	ft_is_negative(int n)
{
	g_pos = 'P';
	g_neg = 'N';
	if (n >= 0)
	{
		write(1, &g_pos, 1);
	}
	else
	{
		write(1, &g_neg, 1);
	}
}
